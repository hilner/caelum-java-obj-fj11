class Repeticoes {

  public static void main(String[] args) {
 
    // while
    int idade = 15;
    while (idade < 18) {
      System.out.println(idade);
      idade = idade + 1; // ou idade++
    }

    int i = 0;
    while (i < 10) {
      System.out.println(i);
      i++;
    
    }

    for (int i2 = 0; i2 < 10; i2 = i2 + 1) {
      System.out.println("Hello World!");
    }

  }

}
